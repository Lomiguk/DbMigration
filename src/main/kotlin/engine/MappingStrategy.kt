package engine

/**
 * Стратегия кэширования маппинга UUID→BIGINT
 */
enum class MappingStrategy {
    /**
     * Полная предзагрузка всех маппингов перед миграцией
     * - Быстро (0 соединений во время миграции)
     * - Требует памяти (~500MB для 20M записей)
     */
    EAGER,

    /**
     * Ленивая загрузка маппингов по мере необходимости
     * - Медленно (создаёт соединения)
     * - Минимум памяти (~50MB)
     */
    LAZY,

    /**
     * Гибридная стратегия: предзагрузка справочников + ленивая для остальных
     * - Компромисс между скоростью и памятью
     */
    HYBRID
}
